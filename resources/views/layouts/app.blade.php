            var r = /^[0-9]*[1-9][0-9]*$/;
            if (!r.test(nownum)) {
                document.getElementById('num').value = minnum;
            }
            if (nownum < minnum) {
                document.getElementById('num').value = minnum;
            }
            // if(nownum > maxnum ) {
